from lab04 import solveMaze
def test_example():
	maze = [
['+','+','+','+','G','+'],
['+',' ','+',' ',' ','+'],
['+',' ',' ',' ','+','+'],
['+',' ','+','+',' ','+'],
['+',' ',' ',' ',' ','+'],
['+','+','+','+','+','+'] ]
	assert solveMaze(maze, 4, 4) == True
	assert maze == [
['+', '+', '+', '+', 'G', '+'],
['+', 8, '+', 11, 12, '+'],
['+', 7, 9, 10, '+', '+'],
['+', 6, '+', '+', 2, '+'],
['+', 5, 4, 3, 1, '+'],
['+', '+', '+', '+', '+', '+'] ]

def test_solveMaze():
    maze = [
	['+','+','+','+'],
	['+','+','+','+'],
	['+','+',' ','+'],
	['+','+','+','+'],
    ]
    assert solveMaze(maze, 2, 2) == False
    assert maze == [
	['+','+','+','+'],
	['+','+','+','+'],
	['+','+',1,'+'],
	['+','+','+','+'],
    ]

